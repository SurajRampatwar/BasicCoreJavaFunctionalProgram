public class QuotientReminder {
    public static void main(String[] args) {
        int number = 36;
        int devisor = 5;
        int Quotient = number / devisor;
        int Reminder = number % devisor;
        System.out.println("Quotient = " + Quotient);
        System.out.println("Reminder = " + Reminder);

    }
}
